# R-Squared: 
# The  baseline metric that ML tools report on; higher R2 values
# signify that the model is “highly predictive”.  

# R2 of > 0.90 means that the model accounts for ~90% of the data variability.


# MSE (Mean Squared Error): 
#  This measures the average of the squares of the errors or deviations.
